/**
 * Funcion para sumar dos numeros.
 *
 * Descripcion detallada de la funcion.
 *
 * @param int $num1 El primer numero.
 * @param int $num2 El segundo numero.
 * @return int La suma de los dos numeros.
 */
function sumar($num1, $num2) {
    return $num1 + $num2;
}

/**
 * Funcion para restar dos numeros.
 *
 * Descripcion detallada de la funcion.
 *
 * @param int $num1 El primer numero.
 * @param int $num2 El segundo numero.
 * @return int La resta de los dos numeros.
 */
function restar($num1, $num2) {
    return $num1 - $num2;
}

